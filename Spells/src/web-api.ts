export class WebAPI {
  isRequesting = false;

  getSpellList() {
    this.isRequesting = true;
    return new Promise(resolve => {
      setTimeout(() => {
        resolve(spells);
        this.isRequesting = false;
      }, latency);
    });
  }

  getSpellDetails(id) {
    this.isRequesting = true;
    return new Promise(resolve => {
      setTimeout(() => {
        let found = spells.filter(x => x.id == id)[0];
        resolve(JSON.parse(JSON.stringify(found)));
        this.isRequesting = false;
      }, latency);
    });
  }
}
